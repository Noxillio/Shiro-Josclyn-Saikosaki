const fs = require('fs');
const { SlashCommandBuilder } = require('@discordjs/builders');
const { REST } = require('@discordjs/rest');
const { Routes } = require('discord-api-types/v9');
const { clientId, guildId, token } = require('./config.json');
const commandFiles = fs.readdirSync('./commands').filter(file => file.endsWith('.js'));

for (const file of commandFiles) {
	const command = require(`./commands/${file}`);
	commands.push(command.data.toJSON());
}

const commands = [
	new SlashCommandBuilder().setName('avatar').setDescription('Fetch a specified user\'s avatar.'),
	new SlashCommandBuilder().setName('help').setDescription('Retrieves an embed with (a) helpful button(s).'),
	new SlashCommandBuilder().setName('say').setDescription('A test command.'),
    new SlashCommandBuilder().setName('serverinfo').setDescription('Display current server information.'),
]
	.map(command => command.toJSON());

const rest = new REST({ version: '9' }).setToken(token);

(async () => {
	try {
		await rest.put(
			Routes.applicationGuildCommands(clientId, guildId),
			{ body: commands },
		);

		console.log('Successfully registered application commands.');
	} catch (error) {
		console.error(error);
	}
})();